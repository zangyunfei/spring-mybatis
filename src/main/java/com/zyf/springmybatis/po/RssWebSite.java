package com.zyf.springmybatis.po;

public class RssWebSite {
	private long rssWebsiteId;
	private String rssWebsiteName;
	private String rssWebsiteUrl;

	public long getRssWebsiteId() {
		return rssWebsiteId;
	}

	public void setRssWebsiteId(long rssWebsiteId) {
		this.rssWebsiteId = rssWebsiteId;
	}

	public String getRssWebsiteName() {
		return rssWebsiteName;
	}

	public void setRssWebsiteName(String rssWebsiteName) {
		this.rssWebsiteName = rssWebsiteName;
	}

	public String getRssWebsiteUrl() {
		return rssWebsiteUrl;
	}

	public void setRssWebsiteUrl(String rssWebsiteUrl) {
		this.rssWebsiteUrl = rssWebsiteUrl;
	}
}
